const calculateExpiryDate = (productionDate: string, days: number): string => {
  if (!productionDate) return '';
  const date = new Date(productionDate);
  date.setDate(date.getDate() + days);
  return date.toISOString().split('T')[0];
};

const calculateGoodsShelfLife = (productionDate: string, shelfLifeDaysList: number[]): string => {
  if (!productionDate || shelfLifeDaysList.length === 0) return '-';
  return shelfLifeDaysList
    .map(days => {
      const date = new Date(productionDate);
      date.setDate(date.getDate() + days);
      return date.toISOString().split('T')[0];
    })
    .join(',');
};

